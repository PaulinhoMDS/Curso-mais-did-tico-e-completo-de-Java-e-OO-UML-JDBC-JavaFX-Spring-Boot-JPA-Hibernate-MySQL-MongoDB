package Conversao;

public class ConversaoDolar {
	
	public static double dolar;
	public static double reais;
    public static double quantidade;
    public static double total;
	
	public static double reais() {
	   return total  = quantidade * dolar;
	}
	
	public static double iof() {
		return reais() * 6 / 100;
	}
	public static double total () {
		return reais() + iof ();
		
	}
	

}
